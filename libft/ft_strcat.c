#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int m;
	int n;

	m = 0;
	n = 0;
	while (s1[m])
		m++;
	while (s2[n])
	{
		s1[m] = s2[n];
		m++;
		n++;
	}
	s1[m] = '\0';
	return (s1);
}

#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	int	uni[2];

	ft_memcpy(uni, "\u00F8", 2);
	if (ft_isascii(c))
		write(fd, &c, 1);
	else
	{
		uni[1] = c;
		write(fd, &uni, 2);
	}
}
